export const mapDummyToSupabaseActivity = (dummy: any) => ({
  id: String(dummy.id),
  title: dummy.title,
  description: dummy.description,
  cover_image_url: dummy.image,
  start_date: new Date(dummy.date).toISOString(),
  location: dummy.location,
  volunteer_count: dummy.volunteers,
  volunteer_quota: dummy.slots,
  category: dummy.category,
  status: dummy.status || "published",
  funding_raised: dummy.fundingRaised,
  funding_goal: dummy.fundingGoal,
  allow_item_donation: true,
  community: { id: "dummy-c1", name: dummy.organizer, logo_url: null, is_verified: true },
  reports: dummy.reports || [],
  feedbacks: [],
  items_needed: dummy.itemsNeeded?.map((i: any) => ({ item_name: i.name, target: i.quantity, donated: i.donated, unit_price: i.unitPrice || 0 })) || []
})
